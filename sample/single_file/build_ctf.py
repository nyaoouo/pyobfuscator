def main():
    # 1. parse
    base_src = open(SRC_PATH, encoding="utf-8").read()
    tree = ast.parse(base_src)

    # 2. substitute markers (build-time plugin)
    mapping = load_marker_map(JSON_PATH)
    n = substitute_markers(tree, mapping)
    print("[markers] substituted %d placeholder node(s) using %d key(s) from %s "
          "(values not shown)" % (n, len(mapping), os.path.basename(JSON_PATH)))
    leftover = find_unsubstituted_markers(tree, mapping)
    if leftover:
        print("[markers] WARNING: marker-like literals with NO JSON entry remain:", leftover)

    subst_src = ast.unparse(tree)  # substituted source (NOT printed — may contain real payloads)

    # 3. obfuscate + pack with the FULL kernel-protection stack; embed the decoy.
    decoy_src = open(DECOY_PATH, encoding="utf-8").read()
    popts = dict(seed=SEED, **FLAGS, **PROTECT_FLAGS, decoy_obf_overrides=DECOY_OBF)
    # PYC-ONLY integrity (both hash version-specific co_code, so they are version-locked — which a .pyc
    # already is, at zero portability cost, but a .py is not, so these stay OFF for the text build):
    #   body_cohash      — #4: the BODY self-verifies its own co_code via the attest gates.
    #   cohash_integrity — the LAUNCHER folds its guard fn's co_code hash into the key (tamper -> decoy).
    # The TEXT artifact (popts) keeps only the portable protections, so a 3.14-built .py runs on 3.12.
    popts_pyc = {**popts, "body_cohash": True, "cohash_integrity": True, "require_min_python": False}
    smap_sink = {}   # opt-in sourcemap: text build fills {launcher, body, body_src}
    text = obf_module(subst_src, ModuleObfOptions(output="text", decoy_src=decoy_src,
                                                  emit_sourcemap=True, **popts),
                      sourcemap_out=smap_sink)
    pyc = obf_module(subst_src, ModuleObfOptions(output="pyc", decoy_src=decoy_src, **popts_pyc))
    # analyze the obfuscated body (not the packer) for the report
    html = analyze_html(subst_src, ObfOptions(output="text", seed=SEED, **FLAGS),
                        title="ctf_test - pyobfuscator analyze")
    # the packer SHELL is "obfuscating another program" -> its own page (layers + launcher
    # pass-timeline + region-annotated assembled launcher), built with the SAME packed options.
    protect_page = protect_html(subst_src, ModuleObfOptions(output="text", decoy_src=decoy_src, **popts),
                                title="ctf_test - pyobfuscator protect shell")

    # 5. verify
    ref = _load_text(subst_src, "ctf_subst")     # substituted source = the build reference
    orig = _load_text(base_src, "ctf_orig")      # original (placeholders) — must match (no-ops)
    _restore_trace_apis(); txt = _load_text(text, "ctf_text")      # each obf launcher neuters APIs
    _restore_trace_apis(); pycns = _load_pyc(pyc, "ctf_pyc")       # process-wide; reset between loads
    _restore_trace_apis()

    fails = []

    # Genuine-entry verification via a real SUBPROCESS — authoritative, and works WITH or WITHOUT
    # detect_stack (a real `python file.py` has f_back None, so the real ctf runs).
    _tf = tempfile.NamedTemporaryFile("w", suffix="_ctf.py", delete=False, encoding="utf-8")
    _tf.write(text); _tf.close()
    try:
        for key, want_code, want_correct in ((FLAG, 0, True), ("{wrong}", 1, False),
                                             (DECOY_FAKE_FLAG, 1, False)):
            out, rc = _run_subprocess(_tf.name, key)
            ok = rc == want_code and (("Correct!" in out) == want_correct)
            print("  [%s] subprocess-argv %-22s -> rc=%s %r"
                  % ("OK " if ok else "XX ", key, rc, out.strip()))
            if not ok:
                fails.append(("subprocess-" + key, {}))

        # audit-TOCTOU attack (red-team _solve_harness shape): an audit hook arms on the inner exec,
        # then settrace harvests inner-frame locals. The harvested data must NOT contain the real
        # FLAG. B1 breaks the published '<pyobf>' arm (never arms); the adaptive any-exec arm traces
        # from start -> detect_trace serves the decoy + B3 diverges, so the harvest is decoy/garbage.
        for albl, broad in (("published<pyobf>", False), ("adaptive-any-exec", True)):
            aout, hung = _attack_audit_toctou(_tf.name, "GUESS-not-the-real-flag", broad=broad)
            leaked = (FLAG in aout) or (FLAG.encode("unicode_escape").decode() in aout)
            ok = not leaked
            print("  [%s] audit-TOCTOU %-18s -> hung=%s armed=%s flag_leaked=%s"
                  % ("OK " if ok else "XX ", albl, hung, "__ARMED__True" in aout, leaked))
            if not ok:
                fails.append(("toctou-" + albl, {}))
    finally:
        os.unlink(_tf.name)

    # Genuine `python ctf_test_obf.pyc <flag>` entry — verify the SHIPPED .pyc artifact runs as a
    # standalone sourceless module (previously the .pyc was only ever loaded in-process). body_cohash
    # is active on the pyc build, so this also confirms the body self-cohash genuine path end-to-end.
    # A real `python file.pyc` has f_back None, so detect_stack stays quiet and the REAL ctf runs.
    _pf = tempfile.NamedTemporaryFile("wb", suffix="_ctf.pyc", delete=False)
    _pf.write(pyc); _pf.close()
    try:
        for key, want_code, want_correct in ((FLAG, 0, True), ("{wrong}", 1, False),
                                             (DECOY_FAKE_FLAG, 1, False)):
            out, rc = _run_subprocess(_pf.name, key)
            ok = rc == want_code and (("Correct!" in out) == want_correct)
            print("  [%s] subprocess-PYC %-22s -> rc=%s %r"
                  % ("OK " if ok else "XX ", key, rc, out.strip()))
            if not ok:
                fails.append(("subprocess-pyc-" + key, {}))
    finally:
        os.unlink(_pf.name)

    # In-process behavior. With detect_stack OFF, in-process exec runs the real ctf and we compare
    # ref == text == pyc. With it ON, in-process exec is itself a "foreign" caller -> it must serve
    # the DECOY (the tripwire firing), which we assert instead.
    if not STACK_DETECT:
        subst_changed_behavior = False
        for label, fn in SCEN_ARGV:
            er, eo, et, ep = fn(ref), fn(orig), fn(txt), fn(pycns)
            ok = (er == et == ep)  # hard gate: obfuscation must preserve the SUBSTITUTED program
            print("  [%s] %-22s -> %r" % ("OK " if ok else "XX ", label, er))
            if not ok:
                fails.append((label, dict(ref=er, text=et, pyc=ep)))
            if eo != er:
                subst_changed_behavior = True
        # Interactive scenarios via genuine subprocess (name_vault captures input by value; see
        # SCEN_INTERACTIVE). Compare the text- and pyc-launcher subprocess output to the in-process
        # plain-source reference. Written to temp files for a real `python file` entry.
        _itf = tempfile.NamedTemporaryFile("w", suffix="_ictf.py", delete=False, encoding="utf-8")
        _itf.write(text); _itf.close()
        _ipf = tempfile.NamedTemporaryFile("wb", suffix="_ictf.pyc", delete=False)
        _ipf.write(pyc); _ipf.close()
        try:
            for label, typed, ref_fn in SCEN_INTERACTIVE:
                er = ref_fn(ref)                                   # in-process plain-source reference
                et = _run_subprocess_stdin(_itf.name, typed)       # vaulted text launcher (subprocess)
                ep = _run_subprocess_stdin(_ipf.name, typed)       # vaulted pyc launcher (subprocess)
                # the reference is in-process (no prompt echo to a real tty) — compare on the verdict
                # token, which is what the scenario actually asserts.
                want_correct = "Correct!" in er
                ok = (("Correct!" in et) == want_correct) and (("Correct!" in ep) == want_correct)
                print("  [%s] %-22s -> ref=%r text=%r pyc=%r"
                      % ("OK " if ok else "XX ", label, er.strip(), et.strip(), ep.strip()))
                if not ok:
                    fails.append((label, dict(ref=er, text=et, pyc=ep)))
                if ref_fn(orig) != er:
                    subst_changed_behavior = True
        finally:
            os.unlink(_itf.name); os.unlink(_ipf.name)
        if subst_changed_behavior:
            print("  [note] substitution changes runtime behavior vs the placeholder source "
                  "(expected only if a marker is in a functional position; ctf markers are no-ops)")
    else:
        out_real, _ = _run_argv(txt, FLAG)   # in-process (foreign) exec -> decoy rejects the real flag
        ok = "Correct!" not in out_real
        print("  [%s] detect_stack: in-process (foreign) exec of real flag -> decoy (rejected)"
              % ("OK " if ok else "XX "))
        if not ok:
            fails.append(("stack-inproc", {}))

    # attestation (dump-and-replay): a dumped REAL body, run without the launcher-installed oracle,
    # must diverge and NOT serve the ctf. Only meaningful when attest is on and the in-process
    # capture sees the real body (detect_stack OFF).
    if PROTECT_FLAGS.get("attest") and not STACK_DETECT:
        served = _dump_replay_serves_real(text, FLAG)
        print("  [%s] attest dump-replay: body w/o oracle does NOT accept real flag (diverges)"
              % ("OK " if not served else "XX "))
        if served:
            fails.append(("attest-dump-replay", {}))

    # traced (anti-trace ARMED): a debugger/settrace at load must get the DECOY, not the real ctf.
    text_code = compile(text, "<traced_t>", "exec")
    pyc_code = marshal.loads(pyc[16:])
    for label, code_obj in (("text", text_code), ("pyc", pyc_code)):
        out_real, _ = _run_traced(code_obj, FLAG)              # real flag, under trace
        out_fake, _ = _run_traced(code_obj, DECOY_FAKE_FLAG)   # decoy's fake flag, under trace
        real_rejected = "Correct!" not in out_real             # decoy does NOT accept the real flag
        decoy_accepts = "Correct!" in out_fake                 # decoy accepts its own fake flag
        ok = real_rejected and decoy_accepts
        print("  [%s] traced-%-4s -> real_flag_rejected=%s decoy_accepts_fake=%s"
              % ("OK " if ok else "XX ", label, real_rejected, decoy_accepts))
        if not ok:
            fails.append(("decoy-" + label, {}))

    flag_plain_absent = FLAG not in text and DECOY_FAKE_FLAG not in text
    dep_absent = "pyobfuscator" not in text and "local_call" not in text
    # placeholder keys must NOT survive as plaintext in the obfuscated output
    markers_absent = all(k not in text for k in mapping)
    print("  [%s] flag plaintext absent" % ("OK " if flag_plain_absent else "XX "))
    print("  [%s] standalone (no pyobfuscator/local_call ref)" % ("OK " if dep_absent else "XX "))
    print("  [%s] no raw placeholder keys leak into output" % ("OK " if markers_absent else "XX "))
    for cond, tag in ((flag_plain_absent, "flag-leak"), (dep_absent, "dep"),
                      (markers_absent, "marker-key-leak")):
        if not cond:
            fails.append((tag, {}))

    if fails:
        print("\n!!! BUILD VERIFICATION FAILED:", [f[0] for f in fails])
        return 1

    # 4. emit (only after verification passes)
    _ensure_out_dir()
    open(OUT_PY, "w", encoding="utf-8").write(text)
    open(OUT_PYC, "wb").write(pyc)
    open(OUT_HTML, "w", encoding="utf-8").write(html)
    open(OUT_PROTECT_HTML, "w", encoding="utf-8").write(protect_page)

    # 4b. sourcemap deliverables + fail-loud completeness self-check (every _pyobf_<hex> in an
    # emitted layer must be mapped). The map is the verification substrate for the static-quality
    # fixes; an orphan name means the provenance capture missed something -> fail the build.
    smap_sink["launcher"]["artifact"] = os.path.basename(OUT_PY)
    smap_sink["launcher"]["source"] = os.path.basename(SRC_PATH)
    smap_sink["body"]["artifact"] = os.path.basename(OUT_BODY)
    smap_sink["body"]["source"] = os.path.basename(SRC_PATH)
    dump_sourcemap(smap_sink["launcher"], OUT_MAP)
    open(OUT_BODY, "w", encoding="utf-8").write(smap_sink["body_src"])
    dump_sourcemap(smap_sink["body"], OUT_BODY_MAP)
    # The body payload is always plaintext (body_src); the launcher is plaintext only when NOT
    # outer-compressed (then `text` is the bootstrap wrapper, so its map maps the DECOMPRESSED
    # launcher and can't be grepped here).
    _layers = [("body", smap_sink["body_src"], smap_sink["body"])]
    if not popts.get("compress_output"):
        _layers.insert(0, ("launcher", text, smap_sink["launcher"]))
    else:
        _m = smap_sink["launcher"]; _st = _m["stats"]["states"]
        print("  [OK ] sourcemap launcher: %d names, %d states (%s) [output compressed; map maps the "
              "decompressed launcher]" % (len(_m["names"]), sum(_st.values()), _st))
    for label, src_text, mp in _layers:
        used = set(_HEX_NAME.findall(src_text))
        orphan = used - set(mp["names"])
        st = mp["stats"]["states"]
        if orphan:
            print("\n!!! SOURCEMAP INCOMPLETE (%s): %d orphan name(s): %s"
                  % (label, len(orphan), sorted(orphan)[:10]))
            return 1
        print("  [OK ] sourcemap %s: %d names, %d states (%s), 0 orphans"
              % (label, len(mp["names"]), sum(st.values()), st))

    print("\nBUILD OK. Deliverables written: %s (%dB) / %s (%dB) / %s (%dB) / %s (%dB)" % (
        os.path.basename(OUT_PY), len(text.encode()),
        os.path.basename(OUT_PYC), len(pyc),
        os.path.basename(OUT_HTML), len(html.encode()),
        os.path.basename(OUT_PROTECT_HTML), len(protect_page.encode())))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
