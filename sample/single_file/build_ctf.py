# ---- behavior harness -------------------------------------------------------
# B5's anti_trace_neuter patches sys/threading trace-set APIs PROCESS-WIDE. When we exec a launcher
# IN-PROCESS (verification), those patches leak into this harness, so a later in-process check that
# must install a real tracer (_run_traced) would itself be blackholed. Capture the genuine APIs at
# import (before any launcher runs) and restore them before such checks. (Already-installed audit
# hooks cannot be removed, but they only set their own build's now-dead cell — harmless here.)
import threading as _threading
_REAL_SETTRACE, _REAL_SETPROFILE = sys.settrace, sys.setprofile
_REAL_ADDAUDITHOOK = sys.addaudithook
_REAL_THR_SETTRACE, _REAL_THR_SETPROFILE = _threading.settrace, _threading.setprofile


def _restore_trace_apis():
    # Restore the genuine APIs that an in-process launcher's B5 neuter patched. addaudithook MUST be
    # restored too: otherwise the NEXT in-process launcher's addaudithook call hits the previous
    # launcher's blackhole guard, which trips THAT launcher's poison cell -> its already-defined body
    # then diverges (KeyError on a dispatcher state). Already-installed audit hooks can't be removed,
    # but they only watch settrace/setprofile events, which the in-process behavior checks never raise.
    sys.settrace = _REAL_SETTRACE
    sys.setprofile = _REAL_SETPROFILE
    sys.addaudithook = _REAL_ADDAUDITHOOK
    _threading.settrace = _REAL_THR_SETTRACE
    _threading.setprofile = _REAL_THR_SETPROFILE


def _load_text(code_text, name):
    ns = {"__name__": name}
    exec(compile(code_text, "<%s>" % name, "exec"), ns)
    return ns


def _load_pyc(pyc_bytes, name):
    ns = {"__name__": name}
    exec(marshal.loads(pyc_bytes[16:]), ns)  # 16-byte pyc header (3.7+)
    return ns


def _run_argv(ns, key):
    buf = io.StringIO(); old = sys.argv; sys.argv = ["ctf"] + ([key] if key is not None else [])
    code = None
    try:
        with contextlib.redirect_stdout(buf):
            try:
                ns["main"]()
            except SystemExit as e:
                code = e.code if e.code is not None else 0
    finally:
        sys.argv = old
    return buf.getvalue(), code


def _run_interactive(ns, typed):
    buf = io.StringIO(); oi = builtins.input; builtins.input = lambda *a, **k: typed
    old = sys.argv; sys.argv = ["ctf"]
    try:
        with contextlib.redirect_stdout(buf):
            try:
                ns["main"]()
            except SystemExit:
                pass
    finally:
        builtins.input = oi; sys.argv = old
    return buf.getvalue()


def _run_traced(code_obj, key):
    """Run the packed module with sys.settrace ACTIVE (the _solve_capture.py attack shape).
    Anti-trace is armed, so the launcher must serve the decoy. Returns (stdout, exit_code)."""
    ns = {"__name__": "ctf_traced"}
    def _tr(frame, event, arg):
        return _tr
    _restore_trace_apis()   # undo any prior in-process launcher's B5 neuter so _tr actually installs
    buf = io.StringIO(); old = sys.argv; sys.argv = ["ctf", key]; rc = None
    sys.settrace(_tr)
    try:
        with contextlib.redirect_stdout(buf):
            try:
                exec(code_obj, ns)   # launcher detects the tracer -> decrypts the decoy into ns
                ns["main"]()
            except SystemExit as e:
                rc = e.code if e.code is not None else 0
    finally:
        sys.settrace(None); sys.argv = old
    return buf.getvalue(), rc


def _run_subprocess(py_path, key):
    """Run the emitted launcher as a genuine `python file.py` entry (f_back is None there, so a
    detect_stack tripwire stays quiet and the REAL ctf runs). Returns (stdout, exit_code)."""
    r = subprocess.run([sys.executable, py_path, key], capture_output=True, text=True)
    return r.stdout, r.returncode


def _attack_audit_toctou(py_path, candidate, broad=False, timeout=15.0):
    """Model the red-team audit-TOCTOU attack (../ctf/_solve_harness.py): an audit hook (invisible to
    the anti-debug checklist) arms on the inner `exec` event, then `sys.settrace` harvests the inner
    frame locals (decoder `cs`/`L`, `state`, decoded constants).
      broad=False -> arm on ``co_filename == '<pyobf>'`` (the PUBLISHED harness; B1's randomized inner
                     filename makes it never arm).
      broad=True  -> arm on ANY CodeType exec (adaptive; it then traces from the very start, so the
                     bootstrap detect_trace decrypts the DECOY and B3 diverges -> harvest is decoy).
    Returns (stdout, hung). The build asserts the real FLAG never appears in the harvested locals."""
    arm = ("_co.co_filename.startswith('<') and 'importlib' not in _co.co_filename" if broad
           else "_co.co_filename == '<pyobf>'")
    probe = (
        "import sys, runpy, types\n"
        "H=[]; st={'a':False}\n"
        "def lt(f,e,a):\n"
        "    try:\n"
        "        co=f.f_code\n"
        "        if co.co_filename.startswith('<') and 'importlib' not in co.co_filename:\n"
        "            for n,v in list(f.f_locals.items()):\n"
        "                if isinstance(v,(str,bytes,int,tuple,list)): H.append(repr(v)[:400])\n"
        "    except Exception: pass\n"
        "    return lt\n"
        "def hook(ev,args):\n"
        "    if ev=='exec' and not st['a'] and args:\n"
        "        _co=args[0]\n"
        "        if isinstance(_co,types.CodeType) and (%s):\n"
        "            st['a']=True; sys.settrace(lt)\n"
        "sys.addaudithook(hook)\n"
        "sys.argv=[%r,%r]\n"
        "try: runpy.run_path(%r, run_name='__main__')\n"
        "except SystemExit: pass\n"
        "except BaseException as ex: print('EXC:',type(ex).__name__)\n"
        "finally: sys.settrace(None)\n"
        "sys.stdout.write('__ARMED__%%s__HARVEST__%%r' %% (st['a'], H))\n"
    ) % (arm, py_path, candidate, py_path)
    pf = tempfile.NamedTemporaryFile("w", suffix="_toctou.py", delete=False, encoding="utf-8")
    pf.write(probe); pf.close()
    try:
        r = subprocess.run([sys.executable, pf.name], capture_output=True, text=True, timeout=timeout)
        return r.stdout, False
    except subprocess.TimeoutExpired:
        return "", True   # hang == diverged == protected
    finally:
        os.unlink(pf.name)


def _capture_real_body_src(code_text, timeout=8.0):
    """Capture the decrypted REAL body SOURCE from a CLEAN load (a fresh, untraced subprocess) by
    hooking `zlib.decompress` — NOT a builtin-integrity-checked name, so the launcher stays on the
    real path and does NOT flip to the decoy. (Replacing `builtins.exec`/`compile` would: P1's
    builtin-integrity treats a Python-typed exec/compile as tampering and selects the decoy body,
    so the old exec-spy silently captured the DECOY.) The launcher runs with __name__ != '__main__'
    so the body is decrypted+defined but main() is not invoked. Returns the body source, or None."""
    probe = (
        "import sys, zlib, base64\n"
        "_real = zlib.decompress; _cap = []\n"
        "def _w(*a, **k):\n"
        "    out = _real(*a, **k); _cap.append(out); return out\n"
        "zlib.decompress = _w\n"
        "sys.argv = ['ctf']\n"
        "exec(compile(base64.b64decode(%r).decode('utf-8'), '<launcher>', 'exec'), {'__name__': 'm'})\n"
        "sys.stdout.write('__BODY__' + base64.b64encode(_cap[-1]).decode())\n"
        % (base64.b64encode(code_text.encode()).decode(),)
    )
    pf = tempfile.NamedTemporaryFile("w", suffix="_cap.py", delete=False, encoding="utf-8")
    pf.write(probe); pf.close()
    try:
        r = subprocess.run([sys.executable, pf.name], capture_output=True, text=True, timeout=timeout)
    except subprocess.TimeoutExpired:
        return None
    finally:
        os.unlink(pf.name)
    if r.returncode != 0 or "__BODY__" not in r.stdout:
        return None
    try:
        return base64.b64decode(r.stdout.split("__BODY__", 1)[1].strip()).decode("utf-8")
    except Exception:
        return None


def _dump_replay_serves_real(code_text, flag, timeout=8.0):
    """ATTESTATION CHECK (dump-and-replay): capture the decrypted REAL body source from a clean load
    (via the zlib.decompress hook above — NOT builtins.exec, which P1's builtin-integrity now flags,
    making the old spy capture the decoy), then run that body in a fresh KILLABLE subprocess WITHOUT
    the launcher-installed oracle. Attestation must make it diverge (hang/crash/wrong) so it does NOT
    accept the real flag. Returns True iff the dumped body still prints 'Correct!' for the real flag
    (i.e. protection FAILED). A hang counts as diverged (subprocess timeout) — a wrong-state CFF
    dispatcher busy-loops forever, so the replay MUST be a killable subprocess, never a thread."""
    body_src = _capture_real_body_src(code_text, timeout=timeout)
    if body_src is None:
        return False  # could not capture the body -> cannot replay (do not report a false failure)
    probe = (
        "import sys, io, contextlib, base64\n"
        "sys.argv = ['ctf', %r]\n"
        "src = base64.b64decode(%r).decode('utf-8')\n"
        "buf = io.StringIO(); ns = {'__name__': '__main__'}\n"
        "try:\n"
        "    with contextlib.redirect_stdout(buf):\n"
        "        exec(compile(src, '<replay>', 'exec'), ns)\n"
        "except SystemExit:\n    pass\n"
        "except BaseException:\n    print('__DIVERGED__'); raise SystemExit(0)\n"
        "print('__CORRECT__' if 'Correct!' in buf.getvalue() else '__DIVERGED__')\n"
        % (flag, base64.b64encode(body_src.encode()).decode())
    )
    pf = tempfile.NamedTemporaryFile("w", suffix="_replay.py", delete=False, encoding="utf-8")
    pf.write(probe); pf.close()
    try:
        r = subprocess.run([sys.executable, pf.name], capture_output=True, text=True, timeout=timeout)
    except subprocess.TimeoutExpired:
        return False  # hang == diverged == protected
    finally:
        os.unlink(pf.name)
    return "__CORRECT__" in r.stdout


